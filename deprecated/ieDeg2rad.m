function r = ieDeg2rad(d)
%Convert degrees to radians
%
%  r = ieDeg2rad(d)
%
% See also: ieRad2deg.  
%    Perhaps this routine should also adopt the varargin structure in that
%    function.
%
% Copyright ImagEval Consultants, LLC, 2003.

warning('Deprecated. Use deg2rad instead');
r = (pi/180)*d;

return;
